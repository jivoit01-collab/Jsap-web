function validateTAN() {
    const tanValue = document.getElementById('tan').value
    const tan = document.getElementById('tan')
    const errorDiv = document.getElementById("tan-error")


    if (tanValue.length === 0) {
        tan.style.borderColor = '';
        if (errorDiv) errorDiv.style.display = 'none';
        return;
    }

    if (!CONFIG.TAN_PATTERN.test(tanValue)) {
        tan.style.borderColor = '#dc3545';

        if (errorDiv) {
            errorDiv.textContent = 'Invalid TAN format (e.g : ABCD12345E)';
            errorDiv.style.display = 'block';
            errorDiv.style.color = '#dc3545';
        }
        return;
    }

    tan.style.borderColor = '#28a745';

    if (errorDiv) {
        errorDiv.textContent = 'Valid TAN No.';
        errorDiv.style.display = 'block';
        errorDiv.style.color = '#28a745';
    }

}

