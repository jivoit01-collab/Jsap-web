async function validateContactId(contactId) {
    console.log(`🔍 Validating Contact ID: ${contactId}`);

    if (!contactId || contactId.length < CONFIG.MIN_CONTACT_ID_LENGTH) {
        console.log(`❌ Validation failed: ID too short (${contactId?.length || 0} chars)`);
        return {
            isValid: false,
            message: `Contact ID must be at least ${CONFIG.MIN_CONTACT_ID_LENGTH} characters`
        };
    }

    try {
        console.log(`🌐 API: Calling CheckContactUid for ${contactId}`);
        const response = await fetch(`/api/BPmaster/CheckContactUid?contactUid=${encodeURIComponent(contactId)}`);

        if (!response.ok) {
            throw new Error(`HTTP error! status: ${response.status}`);
        }

        const result = await response.json();
        console.log(`✅ API Response:`, result);

        const existsInCurrentList = contactsList.some(c =>
            c.contactId.toUpperCase() === contactId.toUpperCase()
        );

        let existsInAPI = false;
        if (result.success && result.data && result.data.message) {
            existsInAPI = result.data.message.toLowerCase().includes("already exists");
        }

        const exists = existsInAPI || existsInCurrentList;

        console.log(`   - In API: ${existsInAPI}`);
        console.log(`   - In current contacts: ${existsInCurrentList}`);
        console.log(`   - Final result: ${exists ? 'EXISTS ❌' : 'UNIQUE ✅'}`);

        return {
            isValid: !exists,
            message: exists ? 'Contact ID already exists' : 'Contact ID is unique'
        };
    } catch (err) {
        console.error("❌ API Error:", err);
        const exists = contactsList.some(c =>
            c.contactId.toUpperCase() === contactId.toUpperCase()
        );
        console.log(`⚠️ Using fallback validation: ${exists ? 'EXISTS' : 'UNIQUE'}`);
        return {
            isValid: !exists,
            message: exists ? 'Contact ID already exists (API unavailable)' : 'Contact ID is unique (API unavailable)'
        };
    }
}

async function validateAndShowResult(contactId) {
    console.log(`🔄 Starting validation for: ${contactId}`);

    if (!contactId || contactId.length < CONFIG.MIN_CONTACT_ID_LENGTH) {
        console.log(`⚠️ Invalid ID: too short (${contactId?.length || 0} chars)`);
        showContactIdField();
        if (errorDiv) {
            errorDiv.textContent = `Contact ID must be at least ${CONFIG.MIN_CONTACT_ID_LENGTH} characters`;
            errorDiv.style.display = 'block';
        }
        if (contactIdInput) contactIdInput.style.borderColor = 'red';
        isContactIdValid = false;
        updateAddButtonState();
        return;
    }

    clearTimeout(contactValidationTimeout);

    hideContactIdMessages();
    if (loadingDiv) {
        loadingDiv.style.display = 'block';
        loadingDiv.textContent = 'Checking ID availability...';
    }
    console.log('⏳ Showing loading state');

    contactValidationTimeout = setTimeout(async () => {
        const validation = await validateContactId(contactId);
        if (loadingDiv) loadingDiv.style.display = 'none';

        if (validation.isValid) {
            console.log(`✅ Contact ID is unique: ${contactId}`);

            if (isAutoGenerated && !isEditMode) {
                console.log('🎯 Auto-generated ID is unique - hiding field');
                hideContactIdField();
                isContactIdValid = true;
            } else {
                console.log('✏️ Manually edited ID is unique - showing success');
                showContactIdField();
                if (successDiv) {
                    successDiv.textContent = '✓ Contact ID is unique';
                    successDiv.style.display = 'block';
                }
                if (contactIdInput) contactIdInput.style.borderColor = 'green';
                isContactIdValid = true;
            }
        } else {
            console.log(`❌ Contact ID exists: ${contactId}`);
            showContactIdField();
            if (contactIdInput) {
                contactIdInput.readOnly = false;
                contactIdInput.style.borderColor = 'red';
            }
            if (errorDiv) {
                errorDiv.textContent = 'Contact ID already exists. Please modify the Contact ID.';
                errorDiv.style.display = 'block';
            }
            isContactIdValid = false;
        }
        updateAddButtonState();
    }, CONFIG.VALIDATION_DELAY);
}

