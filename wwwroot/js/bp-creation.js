function validatePaymentField(fieldId) {
    const field = document.getElementById(fieldId);
    const fieldGroup = field.closest('.form-group');
    const submitBtn = document.getElementsByClassName('btn-submit')[0];

    const errorElement = document.getElementById(`${fieldId}-error`) ||
        document.getElementById(`${fieldId.replace('BankName', 'bank-name')}-error`);

    if (!field) {
        console.log(`Field ${fieldId} not found`);
        return true;
    }

    let isValid = true;
    let errorMessage = '';

    fieldGroup.classList.remove('error', 'success');
    if (errorElement) errorElement.style.display = 'none';
    field.style.borderColor = '';

    const value = field.value.trim();

    switch (fieldId) {
        case 'payment-terms':
            if (!value && field.required) {
                isValid = false;
                errorMessage = 'Please select payment terms';

            }
            break;

        case 'price-list':
            if (!value && field.required) {
                isValid = false;
                errorMessage = 'Please select price list';
            }
            break;

        case 'credit-limit':
            if (value && (isNaN(value) || parseFloat(value) < 0)) {
                isValid = false;
                errorMessage = 'Credit limit must be a positive number';
            }
            break;

        case 'BankName':
            if (!value && field.required) {
                isValid = false;
                errorMessage = 'Please select bank name';
            }
            break;

        case 'account-name':
            if (value && (value.length < 2 || value.length > 100)) {
                isValid = false;
                errorMessage = 'Account name must be 2-100 characters';
            }
            if (value && !/^[a-zA-Z\s]+$/.test(value)) {
                isValid = false;
                errorMessage = 'Account name can only contain letters and spaces';
            }
            break;

        case 'account-number':
            const accountRegex = new RegExp(`^[0-9]{${CONFIG.ACCOUNT_NUMBER_MIN},${CONFIG.ACCOUNT_NUMBER_MAX}}$`);
            if (value && !accountRegex.test(value)) {
                isValid = false;
                errorMessage = `Account number must be ${CONFIG.ACCOUNT_NUMBER_MIN}-${CONFIG.ACCOUNT_NUMBER_MAX} digits`;
            }
            break;

        case 'con-acc-number':
            const conAccountRegex = new RegExp(`^[0-9]{${CONFIG.ACCOUNT_NUMBER_MIN},${CONFIG.ACCOUNT_NUMBER_MAX}}$`);
            if (value && !conAccountRegex.test(value)) {
                isValid = false;
                errorMessage = `Account number must be ${CONFIG.ACCOUNT_NUMBER_MIN}-${CONFIG.ACCOUNT_NUMBER_MAX} digits`;
            }
            break;

        case 'ifsc-code':
            if (value && !CONFIG.IFSC_PATTERN.test(value)) {
                isValid = false;
                errorMessage = 'IFSC format: ABCD0123456 (4 letters, 0, 6 alphanumeric)';
            }
            break;
    }

    if (!isValid) {
        fieldGroup.classList.add('error');
        field.style.borderColor = '#dc3545';
        if (errorElement) {
            errorElement.textContent = errorMessage;
            errorElement.style.display = 'block';
            submitBtn.disabled = true;
        }
        console.log(`Validation failed for ${fieldId}: ${errorMessage}`);
    } else if (value || field.type === 'select-one') {
        fieldGroup.classList.add('success');
        field.style.borderColor = '#28a745';
        console.log(`Validation passed for ${fieldId}`);
        if (submitBtn.disabled === true) {
            submitBtn.disabled = false;

        }
    }

    return isValid;
}

function validateAccountNumberMatch() {
    const accountNumber = document.getElementById('account-number');
    const conAccNumber = document.getElementById('con-acc-number');
    const errorElement = document.getElementById('con-acc-number-error');
    const fieldGroup = conAccNumber.closest('.form-group');

    if (!accountNumber || !conAccNumber || !errorElement) return true;

    const accNum = accountNumber.value.trim();
    const conAccNum = conAccNumber.value.trim();

    fieldGroup.classList.remove('error', 'success');
    errorElement.style.display = 'none';
    conAccNumber.style.borderColor = '';

    if (!accNum || !conAccNum) {
        return true;
    }

    const accountRegex = new RegExp(`^[0-9]{${CONFIG.ACCOUNT_NUMBER_MIN},${CONFIG.ACCOUNT_NUMBER_MAX}}$`);
    if (!accountRegex.test(conAccNum)) {
        fieldGroup.classList.add('error');
        conAccNumber.style.borderColor = '#dc3545';
        errorElement.textContent = `Account number must be ${CONFIG.ACCOUNT_NUMBER_MIN}-${CONFIG.ACCOUNT_NUMBER_MAX} digits`;
        errorElement.style.display = 'block';
        console.log('Confirm account number format invalid');
        return false;
    }

    if (accNum !== conAccNum) {
        fieldGroup.classList.add('error');
        conAccNumber.style.borderColor = '#dc3545';
        errorElement.textContent = 'Account numbers do not match';
        errorElement.style.display = 'block';
        console.log('Account numbers do not match');
        return false;
    }

    fieldGroup.classList.add('success');
    conAccNumber.style.borderColor = '#28a745';
    console.log('Account numbers match');
    return true;
}

function validatePaymentForm() {
    console.log('Validating payment form');

    const fieldsToValidate = [
        'payment-terms',
        'price-list',
        'credit-limit',
        'BankName',
        'account-name',
        'account-number',
        'con-acc-number',
        'ifsc-code'
    ];

    let isValid = true;
    const errors = [];

    fieldsToValidate.forEach(fieldId => {
        const field = document.getElementById(fieldId);
        if (field) {
            if (!validatePaymentField(fieldId)) {
                const fieldLabel = field.previousElementSibling.textContent.replace('*', '').trim();
                errors.push(`Invalid ${fieldLabel}`);
                isValid = false;
            }
        }
    });

    if (!validateAccountNumberMatch()) {
        errors.push('Account numbers must match');
        isValid = false;
    }

    console.log(`Payment form validation: ${isValid ? 'VALID' : 'INVALID'}`);
    if (!isValid) {
        console.log('Payment form errors:', errors);
    }

    return { isValid, errors };
}

