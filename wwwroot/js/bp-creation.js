async function loadGroups() {
    const groupSelect = document.getElementById('group');
    if (!groupSelect) {
        console.error('❌ Group select element not found!');
        return;
    }

    groupSelect.innerHTML = '<option value="">Loading...</option>';
    groupSelect.disabled = true;

    try {
        const bpType = getSimpleBpType();
        const isStaff = getIsStaff();
        const url = `/api/BPmaster/GetGroupNameByBPType?company=${companyId}&bpType=${bpType}&isStaff=${isStaff}`;

        console.log('📡 Fetching groups:', url);

        const response = await fetch(url);

        if (!response.ok) {
            throw new Error(`HTTP error! status: ${response.status}`);
        }

        const data = await response.json();


        groupSelect.innerHTML = '<option value="">Select Group</option>';

        // ✅ FIX: Handle direct array response
        let groups = [];

        if (Array.isArray(data)) {
            // Response is a direct array
            groups = data;
        } else if (data.success && Array.isArray(data.data)) {
            // Response is wrapped in { success, data }
            groups = data.data;
        }

        if (groups.length === 0) {
            console.warn('⚠️ No groups available');
            groupSelect.innerHTML = '<option value="">No groups available</option>';
        } else {
            console.log('🔍 First group:', groups[0]);
            console.log('🔍 Available keys:', Object.keys(groups[0]));

            groups.forEach(group => {
                const option = document.createElement('option');
                option.value = group.groupName
                option.textContent = group.groupName
                groupSelect.appendChild(option);
            });

            console.log(`✅ Loaded ${groups.length} groups into dropdown`);
        }

    } catch (error) {
        console.error('❌ Error loading groups:', error);
        groupSelect.innerHTML = '<option value="">Error loading groups</option>';
    } finally {
        groupSelect.disabled = false;
    }
}

