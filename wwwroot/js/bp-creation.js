function editContact(index) {
    if (isEditMode) {
        alert('Please finish editing the current contact before editing another one.');
        console.log('⚠️ Edit blocked - already editing contact at index:', editingContactIndex);
        return;
    }

    enterEditMode();

    if (index < 0 || index >= contactsList.length) {
        console.log(`❌ Invalid contact index: ${index}`);
        return;
    }

    const contact = contactsList[index];
    console.log(`✏️ Editing contact: ${contact.contactId}`);

    if (contactForm && contactForm.style.display === 'none') {
        toggleForm();
    }

    originalContactData = { ...contact };
    editingContactIndex = index;

    const fields = [
        { id: 'contactFirstName', value: contact.firstName, readonly: true },
        { id: 'contactLastName', value: contact.lastName, readonly: true },
        { id: 'contactPhone', value: contact.phone, readonly: true },
        { id: 'contactEmail', value: contact.email, readonly: true },
        { id: 'contactIdInput', value: contact.contactId, readonly: false }
    ];

    fields.forEach(field => {
        const element = document.getElementById(field.id);
        if (element) {
            element.value = field.value;
            element.readOnly = field.readonly;

            if (field.readonly) {
                element.style.backgroundColor = '#f0f0f0';
                element.style.cursor = 'not-allowed';
            } else {
                element.style.backgroundColor = '#fff';
                element.style.cursor = 'text';
            }
        }
    });

    showContactIdField();

    contactsList.splice(index, 1);

    isContactIdValid = true;
    isAutoGenerated = false;

    updateAddButtonState();
    displayContactsTable();

    console.log('✅ Contact loaded for editing (Contact ID editable, other fields readonly)');
}

function deleteContact(index) {
    if (index < 0 || index >= contactsList.length) {
        console.log(`❌ Invalid contact index: ${index}`);
        return;
    }

    const contact = contactsList[index];
    console.log(`🗑️ Delete request for: ${contact.contactId}`);

    if (confirm(`Are you sure you want to delete ${contact.firstName} ${contact.lastName}?`)) {
        contactsList.splice(index, 1);
        displayContactsTable();

        // ✅ FIXED: Added null check
        if (typeof refreshContactAddressList === 'function') {
            refreshContactAddressList();
        }

        console.log('✅ Contact deleted');
        alert('Contact deleted successfully!');
    } else {
        console.log('❌ Delete cancelled');
    }
}

