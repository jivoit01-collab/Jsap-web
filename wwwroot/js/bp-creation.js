function checkUniqueBp() {
    const bpName = document.getElementById('BPname');
    const bpError = document.getElementById('bpname-error');
    const bpSuccess = document.getElementById('bpname-success');

    if (!bpName.value) {
        console.log("Enter to start")
        bpSuccess.style.display = 'none';
        bpError.style.display = 'block';
        bpName.style.border = '1px solid red';
        bpError.style.color = '#dc3545';
        bpError.innerHTML = 'Please enter a valid business partner name';
        return; // Exit early
    }

    if (bpName.value && bpName.value.replaceAll(" ", "").length > 3) {
        const normalizedInput = bpName.value.toUpperCase().replaceAll(" ", "");
        let isExist = false; // Initialize to false

        // Use some() instead of forEach for early exit
        isExist = bpList.some(bp => {
            const normalizedBP = bp.cardName.toUpperCase().replaceAll(" ", "");
            console.log(`Checking ${normalizedBP} with ${normalizedInput}`);
            return normalizedBP === normalizedInput;
        });

        console.log(isExist);

        if (isExist) {
            bpSuccess.style.display = 'none';
            bpError.style.display = 'block';
            bpName.style.border = '1px solid red';
            bpError.style.color = '#dc3545'
            bpError.innerHTML = 'Please enter a unique business partner name';
        } else {
            bpError.style.display = 'none';
            bpName.style.border = '1px solid green';
            bpSuccess.style.display = 'block';
        }
    }
}
function openBPFilter() {
    const company = companyId;
    const type = document.querySelector('input[name="type"]:checked')?.value || '';
    const isStaff = getIsStaff();
    if (!type) return alert("Please select type first");

    const url = `/api/BPmaster/BPGetCardInfo?company=${company}&BPType=${type}&isStaff=${isStaff}`;
    fetch(url)
        .then(res => res.json())
        .then(data => {
            const list = Array.isArray(data) ? data : data.data;
            const tbody = document.querySelector('#bp-filter-table-body'); // Fixed ID
            tbody.innerHTML = '';

            list.forEach(item => {
                const tr = document.createElement('tr');
                tr.innerHTML = `
                    <td>${item.cardName}</td>
                    <td>${item.cardCode || '-'}</td>
                    <td>${item.address}</td>
                `;
                tr.style.cursor = "pointer";
                tbody.appendChild(tr);
            });

            document.getElementById("bp-filter-modal").style.display = "block"; // Fixed ID
        })
        .catch(err => {
            console.error("Error fetching BP info:", err);
            alert("Failed to load BP list");
        });
}

function closeBPFilter() {
    document.getElementById("bp-filter-modal").style.display = "none";
}
function filterBPTable(searchTerm) {
    const table = document.getElementById('bp-filter-table-body');
    if (!table) return;

    table.innerHTML = '';

    const filtered = bpList.filter(bp => {
        if (!bp || !bp.cardName) return false;
        return bp.cardName.toLowerCase().includes(searchTerm.toLowerCase());
    });

    filtered.forEach(bp => {
        const row = document.createElement('tr');

        const nameCell = document.createElement('td');
        nameCell.textContent = bp.cardName || '';

        const codeCell = document.createElement('td');
        codeCell.textContent = bp.cardCode || '';

        const addressCell = document.createElement('td');
        addressCell.textContent = bp.address || '';


        row.appendChild(nameCell);
        row.appendChild(codeCell);
        row.appendChild(addressCell);
        table.appendChild(row);
    });
}


