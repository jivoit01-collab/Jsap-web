function setupEventListeners() {
    console.log('🎯 Setting up core form event listeners');

    // Type selection listeners (Customer/Vendor)
    const typeRadios = document.querySelectorAll('input[name="type"]');
    typeRadios.forEach(radio => {
        radio.addEventListener('change', function () {
            console.log(`Type changed to: ${this.value}`);
            updateCodeField();
            updateFieldVisibilityAndRequirements();
        });
    });

    // Staff selection listeners (Yes/No)
    const staffRadios = document.querySelectorAll('input[name="isStaff"]');
    staffRadios.forEach(radio => {
        radio.addEventListener('change', function () {
            console.log(`Staff status changed to: ${this.value}`);
            updateCodeField();
            updateFieldVisibilityAndRequirements();
        });
    });

    // Branch selection listener
    const branchSelect = document.getElementById('branch');
    if (branchSelect) {
        branchSelect.addEventListener('change', function () {
            console.log(`Branch changed to: ${this.value}`);
            updateFieldVisibilityAndRequirements();
        });
    }

    // PAN input listener
    const panInput = document.getElementById('pan');
    if (panInput) {
        panInput.addEventListener('input', function () {
            this.value = this.value.toUpperCase();
            validatePANField();

            panNumber = this.value.toUpperCase();
            console.log(`PAN Number updated to : ${panNumber}`)

            const gstPanInput = document.getElementById('billing-gst-pan')
            if (!gstPanInput) return;

            if (gstPanInput.value != panNumber) {
                gstPanInput.value = panNumber
                console.log(`GstPan Number Updated to ${panNumber}`)
            }
        });
    }

    // TAN availability listener
    const tanRadios = document.querySelectorAll('input[name="hasTAN"]');
    tanRadios.forEach(radio => {
        radio.addEventListener('change', function () {
            updateFieldVisibilityAndRequirements();
        });
    });

    // MSME Number availability listener
    const msmeRadios = document.querySelectorAll('input[name="hasMSMENum"]');
    msmeRadios.forEach(radio => {
        radio.addEventListener('change', function () {
            updateFieldVisibilityAndRequirements();
        });
    });

    console.log('✅ Core form event listeners setup complete');
}








