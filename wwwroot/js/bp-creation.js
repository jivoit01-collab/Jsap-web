async function validatePostalForContact(contactIndex, addressType) {
    const zipInput = document.getElementById(`${addressType}-zip-${contactIndex}`);
    const stateSelect = document.getElementById(`${addressType}-state-${contactIndex}`);
    const errorDiv = document.getElementById(`${addressType}-zip-${contactIndex}-error`);

    if (!zipInput || !stateSelect || !errorDiv) return;

    const postalCode = zipInput.value.trim();

    if (postalValidationTimeout) {
        clearTimeout(postalValidationTimeout);
    }

    errorDiv.style.display = 'none';
    zipInput.style.borderColor = '';

    if (!postalCode || postalCode.length < CONFIG.POSTAL_CODE_LENGTH) return;

    if (!CONFIG.POSTAL_CODE_PATTERN.test(postalCode)) {
        errorDiv.textContent = `Postal code must be ${CONFIG.POSTAL_CODE_LENGTH} digits`;
        errorDiv.style.display = 'block';
        zipInput.style.borderColor = 'red';
        return;
    }

    const selectedState = stateSelect.value;
    if (!selectedState) return;

    errorDiv.textContent = 'Validating postal code...';
    errorDiv.style.display = 'block';
    errorDiv.style.color = '#666';

    postalValidationTimeout = setTimeout(async () => {
        try {
            const response = await fetch(`https://api.postalpincode.in/pincode/${postalCode}`);
            if (!response.ok) throw new Error('API error');

            const data = await response.json();

            if (!data || data.length === 0 || data[0].Status === 'Error') {
                errorDiv.textContent = 'Invalid postal code';
                errorDiv.style.color = '#dc3545';
                zipInput.style.borderColor = '#dc3545';
                return;
            }

            const apiState = data[0].PostOffice[0].State;
            const stateSelect = document.getElementById(`${addressType}-state-${contactIndex}`);
            const selectedOption = stateSelect.options[stateSelect.selectedIndex];
            const selectedStateName = selectedOption.textContent.trim();

            if (apiState.toLowerCase().trim() === selectedStateName.toLowerCase().trim()) {
                errorDiv.textContent = `✓ Valid postal code for ${apiState}`;
                errorDiv.style.color = '#28a745';
                zipInput.style.borderColor = 'green';
            } else {
                errorDiv.textContent = `Postal code belongs to ${apiState}, but you selected ${selectedStateName}`;
                errorDiv.style.color = '#dc3545';
                zipInput.style.borderColor = 'red';
            }
        } catch (error) {
            console.error('Postal validation error:', error);
            errorDiv.textContent = 'Unable to validate postal code';
            errorDiv.style.color = '#dc3545';
        }
    }, 800);
}


function validateAddressFormForContact(contactIndex) {
    const sameAsBilling = document.getElementById(`same-as-billing-${contactIndex}`).checked;
    const errorDiv = document.querySelectorAll('.error-message');


    const requiredFields = [
        'billing-address1', 'billing-country', 'billing-state',
        'billing-city', 'billing-zip'
    ];

    if (!sameAsBilling) {
        requiredFields.push(
            'shipping-address1', 'shipping-country', 'shipping-state',
            'shipping-city', 'shipping-zip'
        );
    }
    let isValid = true;
    const errors = [];

    requiredFields.forEach(fieldId => {
        const input = document.getElementById(`${fieldId}-${contactIndex}`);
        if (!input) return;

        const value = input.value.trim();
        if (!value) {
            isValid = false;
            errors.push(`${fieldId.replace('-', ' ')} is required`);
        }
    });

    console.log(errorDiv)
    if (errorDiv.length > 0) {
        errors.push('Invalid GST formats')
    }

    if (!contactAddressIdValidation[contactIndex]) {
        isValid = false;
        errors.push('Address IDs are not validated');
    } else {
        if (!contactAddressIdValidation[contactIndex].billing) {
            isValid = false;
            errors.push('Billing Address ID is not valid');
        }
        if (!sameAsBilling && !contactAddressIdValidation[contactIndex].shipping) {
            isValid = false;
            errors.push('Shipping Address ID is not valid');
        }
    }

    console.log(`Address form validation for contact ${contactIndex}: ${isValid ? 'VALID' : 'INVALID'}`);
    if (!isValid) {
        console.log('Errors:', errors);
    }

    return { isValid, errors };
}

