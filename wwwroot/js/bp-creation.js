async function loadBPList() {
    console.log('📥 Loading BP list for filter');
    try {
        const response = await fetch(`/api/BPmaster/BPGetCardInfo?company=${companyId}&BPType=${getBpType()}&isStaff=${getIsStaff()}`);
        if (!response.ok) {
            throw new Error(`HTTP error! status: ${response.status}`);
        }
        const data = await response.json();
        if (data.success && Array.isArray(data.data)) {
            bpList = data.data;
            console.log(`✅ Loaded ${bpList.length} Business Partners`);
        } else {
            console.warn('No BPs loaded or unexpected response format');
            bpList = [];
        }
    } catch (error) {
        console.error('❌ Error loading BP list:', error);
        bpList = [];
    }
}

