async function validateAddressIdForContact(contactIndex, addressType, addressId) {
    try {
        console.log(`Validating ${addressType} Address ID for contact ${contactIndex}: ${addressId}`);

        const response = await fetch(`/api/BPmaster/CheckAddressUid?addressUid=${encodeURIComponent(addressId)}`);

        if (!response.ok) {
            throw new Error(`HTTP error! status: ${response.status}`);
        }

        const result = await response.json();

        const existsInCurrentList = addressesList.some(a =>
            a.billingAddressId?.toUpperCase() === addressId.toUpperCase() ||
            a.shippingAddressId?.toUpperCase() === addressId.toUpperCase()
        );

        let existsInAPI = false;
        if (result.success && result.data && result.data.message) {
            existsInAPI = result.data.message.toLowerCase().includes("already exists");
        }

        const exists = existsInAPI || existsInCurrentList;

        if (!exists) {
            const inputWrapper = document.getElementById(`${addressType}-address-id-input-wrapper-${contactIndex}`);
            const errorDiv = document.getElementById(`${addressType}-address-id-${contactIndex}-error`);

            console.log(`✅ ${addressType} Address ID is unique: ${addressId}`);
            if (!contactAddressIdValidation[contactIndex]) {
                contactAddressIdValidation[contactIndex] = {};
            }
            contactAddressIdValidation[contactIndex][addressType] = true;


            if (errorDiv) {
                errorDiv.textContent = `${addressId} is Unique`;
                errorDiv.style.display = 'block';
                errorDiv.style.color = 'green'
            }
        } else {
            console.log(`❌ ${addressType} Address ID exists: ${addressId}`);
            const inputWrapper = document.getElementById(`${addressType}-address-id-input-wrapper-${contactIndex}`);
            const errorDiv = document.getElementById(`${addressType}-address-id-${contactIndex}-error`);

            if (inputWrapper) inputWrapper.style.display = 'block';
            if (errorDiv) {
                errorDiv.textContent = 'Address ID already exists. Please modify.';
                errorDiv.style.display = 'block';
                errorDiv.style.color = '#dc3545';
            }

            if (!contactAddressIdValidation[contactIndex]) {
                contactAddressIdValidation[contactIndex] = {};
            }
            contactAddressIdValidation[contactIndex][addressType] = false;
        }
    } catch (error) {
        console.error(`Error validating ${addressType} address ID:`, error);
    }
}

