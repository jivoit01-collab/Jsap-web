function renderAttachments() {
    const container = document.getElementById('attachments-list');
    if (!container) {
        console.error('Attachments list container not found');
        return;
    }

    console.log('Rendering attachments, count:', attachments.length);
    container.innerHTML = '';

    if (attachments.length === 0) {
        container.innerHTML = '<p style="color: #666; text-align: center; padding: 20px; margin: 0;">No files uploaded</p>';
        return;
    }

    attachments.forEach((file, index) => {
        const item = document.createElement('div');
        item.className = 'attachment-item';

        const fileExt = file.name.split('.').pop().toLowerCase();
        const fileIcon = getFileIcon(fileExt);

        const infoDiv = document.createElement('div');
        infoDiv.className = 'attachment-info';

        const iconSpan = document.createElement('span');
        iconSpan.className = 'file-icon';
        iconSpan.textContent = fileIcon;

        const detailsDiv = document.createElement('div');
        detailsDiv.className = 'file-details';

        const nameDiv = document.createElement('div');
        nameDiv.className = 'file-name';
        nameDiv.textContent = file.name;

        const sizeDiv = document.createElement('div');
        sizeDiv.className = 'file-size';
        sizeDiv.textContent = `${(file.size / 1024).toFixed(1)} KB`;

        detailsDiv.appendChild(nameDiv);
        detailsDiv.appendChild(sizeDiv);
        infoDiv.appendChild(iconSpan);
        infoDiv.appendChild(detailsDiv);

        const btn = document.createElement('button');
        btn.type = 'button';
        btn.className = 'remove-attachment-btn';
        btn.onclick = () => removeAttachment(index);
        btn.innerHTML = `
            <svg width="16" height="16" viewBox="0 0 24 24" fill="currentColor">
                <path d="M6 19c0 1.1.9 2 2 2h8c1.1 0 2-.9 2-2V7H6v12zM19 4h-3.5l-1-1h-5l-1 1H5v2h14V4z"/>
            </svg>
            Remove
        `;

        item.appendChild(infoDiv);
        item.appendChild(btn);
        container.appendChild(item);
    });

    console.log('Attachments rendered successfully');
}

